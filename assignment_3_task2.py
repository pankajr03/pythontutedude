import math
entered_number = int(input('Enter a number: '))
square_number = math.sqrt(entered_number)
natural_logarithm_number = math.log(entered_number)
sine_number = math.sin(entered_number)

print(f'Square root: {square_number}')
print(f'Logarithm: {natural_logarithm_number}')
print(f'Sine: {sine_number}')
